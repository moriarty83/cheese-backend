//////////////////////
// DEPENDENCIES
//////////////////////
const express = require("express");
const middleware = require("./util/middleware");
require('dotenv').config();

//////////////////////
// APPLICATION
//////////////////////
const app = express();

//////////////////////
// PORT
//////////////////////
const {PORT = 3000} = process.env;

//////////////////////
// MIDDLEWARE
//////////////////////
middleware(app);

//////////////////////
// ROUTES
//////////////////////

//////////////////////
// LISTENER
//////////////////////
app.listen(PORT, ()=>{
    console.log("Hello Seattle, I'm listening on "+PORT)
})